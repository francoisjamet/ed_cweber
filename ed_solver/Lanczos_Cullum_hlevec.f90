  SUBROUTINE init_Lanczos_Cullum_hlevec
    MBETA_  = 10000  ! ALWAYS FINE 
    MBOUND_ = 0      ! WE WILL GUESS MDIMTV BEFOREHAND...
    MDIMTV_ = 10000  ! ... BUT IT MAYBE WRONG
    NTVCON_ = 0
    SVTVEC_ = 0
    IREAD_  = 1
    TVSTOP_ = 0
    LVCONT_ = 1
    ERCONT_ = 1
    IWRITE_ = 0
    MATNO_  = 1
    RELTOL_ = 10.0_DBL*tolerance
  END SUBROUTINE


