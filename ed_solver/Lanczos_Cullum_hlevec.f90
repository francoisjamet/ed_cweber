     SUM = ONE/DSQRT(SUM)
     DO 950 I = 1,N
       V1(I) = ZEROC
       950 V2(I) = V2(I)*SUM

       !     LOOP FOR GENERATING REQUIRED RITZ VECTORS (IVEC = 1,KMAXU)
       !     USES GRAM-SCHMIDT ORTHOGONALIZATION WITHOUT MODIFICATION

       IVEC = 1
       BATA = ZERO

       GO TO 1010

       960 CONTINUE

       !-----------------------------------------------------------------------
       !  CALCULATES  VS = A*V2 - SUM*VS
       SUM = ZERO
       CALL CMATVEC__(V2(1:N),VS(1:N),SUM)
       ALFA = DBLE(MPI_DOT_PRODUCT(V2(1:N),VS(1:N))) 

       !-----------------------------------------------------------------------
       DO 970 J=1,N
         970 V1(J) = (VS(J) - BATA*V1(J)) - ALFA*V2(J)

         !-----------------------------------------------------------------------
         BATA = DBLE(MPI_DOT_PRODUCT(V1(1:N),V1(1:N))) 
         !-----------------------------------------------------------------------

         BATA = DSQRT(BATA)
         SUM  = ONE/BATA

         TEMP = BETA(IVEC)
         TEMP = DABS(BATA - TEMP)/TEMP
         IF (TEMP.LT.1.0D-10)GO TO 990

         !     THE BETA BEING REGENERATED DO NOT MATCH THE HISTORY FILE
         !     SOMETHING IS WRONG IN THE LANCZOS VECTOR GENERATION
         !     PROGRAM TERMINATES FOR USER TO CORRECT THE PROBLEM
         !     WHICH MUST BE IN THE STARTING VECTOR GENERATION OR IN
         !     THE MATRIX-VECTOR MULTIPLY SUBROUTINE CMATVEC SUPPLIED.
         !     THIS SUBROUTINE MUST BE THE SAME ONE USED IN THE
         !     EIGENVALUE COMPUTATIONS OR A MISMATCH WILL ENSUE.
         !
         WRITE(6,980) IVEC,BATA,BETA(IVEC),TEMP
         980 FORMAT(/2X,'IVEC',16X,'BATA',10X,'BETA(IVEC)',14X,'RELDIF'/I6,3E20.12/&
         ' IN LANCZOS VECTOR REGENERATION THE ENTRIES OF THE TRIDIAGONAL MATRICES BEING'/&
         ' GENERATED ARE NOT THE SAME AS THOSE IN THE MATRIX SUPPLIED ON FILE 2.'/&
         ' THEREFORE SOMETHING IS BEING INITIALIZED OR COMPUTED DIFFERENTLY FROM THE WAY'/&
         ' IT WAS COMPUTED IN THE EIGENVALUE COMPUTATIONS'/&
         ' THE PROGRAM TERMINATES FOR THE USER TO DETERMINE WHAT THE PROBLEM IS'/)
         GO TO 1630
 

         990 CONTINUE
         DO 1000 J = 1,N
           TEMPC = SUM*V1(J)
           V1(J) = V2(J)
           1000 V2(J) = TEMPC

           1010 CONTINUE

           LFIN = 0
           DO 1030 J = 1,NGOODC
      LL = LFIN
      LFIN = LFIN + N
      !
      IF(IABS(MA(J)).LT.IVEC.OR.MP(J).EQ.MPMIN) GO TO 1030
      II = IVEC + MINT(J) - 1
      TEMP = TVEC(II)
      !     II IS THE (IVEC)TH COMPONENT OF THE T-EIGENVECTOR CONTAINED
      !     IN TVEC(MINT(J)).
      !
      DO 1020 K = 1,N
        LL = LL + 1
        1020 RITVEC(LL) = TEMP*V2(K) + RITVEC(LL)
        !
        1030 CONTINUE
        !
        IVEC = IVEC + 1
        IF (IVEC.LE.KMAXU) GO TO 960
        !
        !
        !     RITZVECTOR GENERATION IS COMPLETE. NORMALIZE EACH RITZVECTOR.
        !     NOTE THAT IF CERTAIN RITZ VECTORS WERE NOT COMPUTED THEN THAT
        !     PORTION OF THE RITVEC ARRAY WAS NOT UTILIZED.
        !
        LFIN = 0
        DO 1130 J = 1,NGOODC
          !
          KK = LFIN
          LFIN = LFIN + N
          IF(MP(J).EQ.MPMIN) GO TO 1130
          !
          DO 1040 K = 1,N
            KK = KK + 1
            1040 V2(K) = RITVEC(KK)
  
            !-----------------------------------------------------------------------
            SUM = DBLE(MPI_DOT_PRODUCT(V2(1:N),V2(1:N))) 
            !-----------------------------------------------------------------------

            SUM = DSQRT(SUM)
            RNORM(J) = SUM
            TEMP = DABS(ONE-SUM)
            SUM = ONE/SUM
            !
            KK = LFIN - N
            DO 1050 K = 1,N
       KK = KK + 1
       V2(K) = SUM*V2(K)
       1050 RITVEC(KK) = V2(K)
       !
       !    ONLY ENTER NEXT PORTION IF GIVEN MATRIX IS REAL.
       IF(ISREAL.NE.1) GO TO 1100
       !
       !    AT THIS POINT RITZ VECTOR IS IN V2.
       !    THIS PROGRAM CAN BE USED ON REAL MATRICES TO DETERMINE
       !    WHICH IF ANY EIGENVALUES ARE A-MULTIPLE AND IF SO TO COMPUTE
       !    TWO EIGENVECTORS FOR THOSE EIGENVALUES THAT ARE MULTIPLE AND ONE
       !    FOR THOSE THAT ARE NOT MULTIPLE. HERE ONLY IDENTIFIES WHETHER
       !    EIGENVALUE IS AT LEAST DOUBLE.  THIS IS DONE BY CHECKING THE
       !    RATIOS OF SUCCEEDING REAL AND IMAGINARY PARTS OF THE COMPUTED
       !    RITZ VECTORS.
       !
       SUM = AIMAG(V2(1))/DBLE(V2(1))
       DO 1060 K=2,N
         TEMP = DBLE(V2(K))
         IF(DABS(TEMP).LT.1.D-9) GO TO 1060
         TEMP = AIMAG(V2(K))/DBLE(V2(K))
         IF(DABS(TEMP - SUM).LE.1.D-6) GO TO 1060
         MULEVA(J) = 2
         GO TO 1070
         1060 CONTINUE
         MULEVA(J) = 1
         1070 IF(MULEVA(J).EQ.2)THEN; IF(.NOT.silent_) WRITE(6,1090) J,GOODEV(J); ENDIF
           IF(MULEVA(J).EQ.1)THEN; IF(.NOT.silent_) WRITE(6,1080) J,GOODEV(J); ENDIF
             1080 FORMAT(I6,'TH EIGENVALUE CONSIDERED =',E20.12,' IS SIMPLE')
             1090 FORMAT(I6,'TH EIGENVALUE CONSIDERED =',E20.12,' IS MULTIPLE')
             !
             1100 CONTINUE
             !
             IF (IWRITE.NE.0)THEN; IF(.NOT.silent_) WRITE(6,1110) J,GOODEV(J); ENDIF
        1110 FORMAT(/I5,' TH EIGENVALUE CONSIDERED = ',E20.12/)

        IF (IWRITE.NE.0)THEN; IF(.NOT.silent_) WRITE(6,1120) TERR(J),TBETA(J),TEMP; ENDIF
          1120 FORMAT(' NORM OF ERROR IN T-EIGENVECTOR = ',E14.3/&
          ' BETA(MA(J)+1)*U(MA(J))  = ',E14.3/&
          ' ABS(NORM(RITVEC) - 1.0)  = ',E14.3/)

          LINT = LFIN - N + 1
          EVAL = EVNEW(J)
          !
          !-----------------------------------------------------------------------
          CALL CMATVEC__(RITVEC(LINT:LINT+N-1),V2(1:N),EVAL)
          !-----------------------------------------------------------------------
          !
          !     COMPUTE ERROR IN RITZ VECTOR CONSIDERED AS A EIGENVECTOR OF A.
          !     V2 = A*RITVEC - EVAL*RITVEC
          !
          !-----------------------------------------------------------------------
          SUM = DBLE(MPI_DOT_PRODUCT(V2(1:N),V2(1:N))) 
          !-----------------------------------------------------------------------
          !
          SUM = DSQRT(SUM)
          ERR(J) = SUM
          GAP = ABS(AMINGP(J))
          ERRDGP(J) = SUM/GAP
          !
          1130 CONTINUE
          !
          !     RITZVECTORS ARE NORMALIZED AND ERROR ESTIMATES ARE IN ERR ARRAY
          !     AND IN ERRDGP ARRAY. STORE EVERYTHING
          !
          WRITE(9,1140)
          1140 FORMAT(6X,'GOODEV(J)',1X,'MA(J)',4X,'A MINGAP',6X,'AERROR',2X,'AERROR/GAP',6X,'TERROR')
          !
          WRITE(13,1150)
          1150 FORMAT(16X,'GOODEV(J)',5X,'RITZNORM',6X,'AMINGAP',5X,'TBETA(J)',5X,'TLAST(J)')
          !
          DO 1180 J=1,NGOODC
            !
            IF(MP(J).EQ.MPMIN) GO TO 1180
            !
            WRITE(9,1160)EVNEW(J),MA(J),AMINGP(J),ERR(J),ERRDGP(J),TERR(J)
            1160 FORMAT(E15.8,I6,4E12.4)
            !
            WRITE(13,1170) EVNEW(J),RNORM(J),AMINGP(J),TBETA(J),TLAST(J)
            1170 FORMAT(E25.14,4E13.5)
            !
            1180 CONTINUE
            !
            IF(MREJEC.EQ.0) GO TO 1260
            WRITE(9,1190)
            1190 FORMAT(/' RITZ VECTORS WERE NOT COMPUTED FOR THE FOLLOWING EIGENVALUES'/&
            ' EITHER BECAUSE THEY HAD NOT CONVERGED OR BECAUSE THE ERROR ESTIMATE'/&
            '  WAS NOT AS SMALL AS DESIRED'/)
            !
            DO 1250 J = 1,NGOODC
              IF(MP(J).NE.MPMIN) GO TO 1250
              !     WRITE OUT MESSAGE FOR EACH EIGENVALUE FOR WHICH NO EIGENVECTOR
              !     WAS COMPUTED.
              !
              WRITE(9,1200)
              1200 FORMAT(6X,'GOODEV(J)',3X,'MA(J)',5X,'AMINGP(J)',6X,'TLAST(J)',3X,'MP(J)')
              WRITE(9,1210) GOODEV(J),MA(J),AMINGP(J),TBETA(J),MP(J)
              1210 FORMAT(E15.8,I8,2E14.4,I8)
              !
              WRITE(13,1220)
              1220 FORMAT(/' RITZ VECTORS WERE NOT COMPUTED FOR THE FOLLOWING EIGENVALUES'/&
              ' EITHER BECAUSE THEY HAD NOT CONVERGED OR BECAUSE'/&
              ' THE ERROR ESTIMATE WAS NOT AS SMALL AS DESIRED'/)
              !
              WRITE(13,1230)
              1230 FORMAT(6X,'GOODEV(J)',3X,'MA(J)',3X,'M1(J)',3X,'M2(J)',3X,'MP(J)'/)
              WRITE(13,1240) GOODEV(J),MA(J),M1(J),M2(J),MP(J)
              1240 FORMAT(E15.8,4I8)
              !
              1250 CONTINUE
              1260 CONTINUE
              !
              WRITE(9,1270)
              1270 FORMAT(/' ABOVE ARE ERROR ESTIMATES FOR THE A AND T EIGENVECTORS'/&
              ' ASSOCIATED WITH THE GOODEV LISTED IN COLUMN 1'/&
              ' AERROR = NORM(A*X - EV*X)  TERROR = NORM(T*Y - EV*Y) '/&
              ' WHERE T = T(1,MA(J))    X = RITZ VECTOR = V*Y  V = SUCCESSIVE'/&
              ' LANCZOS VECTORS. A MINGAP = GAP TO NEAREST A-EIGENVALUE'//)
    
              WRITE(13,1280)
              1280 FORMAT(/' ABOVE ARE ERROR ESTIMATES ASSOCIATED WITH THE GOODEV'/&
              ' RITZNORM = NORM(RITZ VECTOR)'/&
              ' TBETA(J) = CDABS(BETA(MA(J)+1)*Y(MA(J))),  T*Y = GOODEV*Y'/&
              ' TLAST(J) = CDABS(Y(MA(J))'/&
              ' AMINGAP = DISTANCE TO CLOSEST COMPUTED GOOD T-EIGENVALUE'/)
              !
              !     NUMBER OF RITZ VECTORS COMPUTED
              NCOMPU = NGOODC - MREJEC

              LFIN = 0
              DO 1350 J = 1,NGOODC
         LINT = LFIN + 1
         LFIN = LFIN + N
         !
         IF(MP(J).EQ.MPMIN) GO TO 1330
         !     RITZ VECTOR WAS COMPUTED
         !
         GO TO 1350
         !     NO RITZ VECTOR WAS COMPUTED FOR THIS EIGENVALUE
         1330 CONTINUE 

         1350 CONTINUE

         !     DID ANY T-MATRICES INCLUDE OFF-DIAGONAL ENTRIES SMALLER THAN
         !     DESIRED, AS SPECIFIED BY BTOL?

         IF(IB.GT.0) GO TO 1380
         IF(.NOT.silent_) WRITE(6,1360) KMAXU
         1360 FORMAT(/' FOR LARGEST T-MATRIX CONSIDERED',I7,' CHECK THE SIZE OF BETAS')

         !-----------------------------------------------------------------------
         CALL TNORM(ALPHA,BETA,BKMIN,TEMP,KMAXU,IBMT,SILENT=silent_)
         !-----------------------------------------------------------------------

         IF(IBMT.LT.0) WRITE (6,1370)
         1370 FORMAT(/' WARNING THE T-MATRICES FOR ONE OR MORE OF THE EIGENVALUES CONSIDERED'/&
         ' HAD AN OFF-DIAGONAL ENTRY THAT WAS SMALLER THAN THE BETA TOLERANCE THAT WAS SPECIFIED'/)
         1380 CONTINUE
         !
         GO TO 1630

         1390 WRITE(6,1400) NGOOD,NMAX,MDIMRV
         1400 FORMAT(/I4,' RITZ VECTORS WERE REQUESTED BUT THE REQUIRED DIMENSION',I6/&
         ' IS LARGER THAN THE USER-SPECIFIED DIMENSION OF RITVEC',I6/&
         ' THEREFORE, THE EIGENVECTOR PROCEDURE TERMINATES FOR THE USER TO INTERVENE')

         GO TO 1630

         1410 WRITE(6,1420) NOLD,N,MATOLD,MATNO
         1420 FORMAT(/' PARAMETERS READ FROM FILE 3 DO NOT AGREE WITH THOSE SPECIFIED'/&
         ' BY THE USER.   NOLD,N,MATOLD,MATNO = '/2I6,2I12/&
         ' THEREFORE, PROGRAM TERMINATES FOR USER TO RESOLVE THE DIFFERENCES'/)
         !
         GO TO 1630

         1430 WRITE(6,1440)
         1440 FORMAT(/' PARAMETERS IN ALPHA,BETA FILE READ IN DO NOT AGREE WITH THOSE'/&
         ' SPECIFIED BY THE USER.  THEREFORE, THE PROCEDURE TERMINATES'/&
         ' FOR THE USER TO RESOLVE THE DIFFERENCES.'/)
         !
         GO TO 1630

         1450 WRITE(6,1460) KMAX,MEV
         1460 FORMAT(/' ON ALPHA,BETA HEADER KMAX = ',I6/&
         ' BUT EIGENVALUES WERE COMPUTED AT MEV = ',I6,' PROGRAM STOPS'/)

         GO TO 1630
         !
         1470 WRITE(6,1480)
         1480 FORMAT(/' PROGRAM COMPUTED 1ST GUESSES ON T-MATRIX SIZES, READ THEM TO FILE 10'/&
         ' THEN TERMINATED AS REQUESTED.')
         GO TO 1630
         !
         1490 WRITE(6,1500) MTOL, MDIMTV
         1500 FORMAT(/' PROGRAM TERMINATES BECAUSE THE TVEC DIMENSION ANTICIPATED',I7/&
         '  IS LARGER THAN THE TVEC DIMENSION',I7,' SPECIFIED BY THE USER.'/&
         '  USER MAY RESET THE TVEC DIMENSION AND RESTART THE PROGRAM')
         GO TO 1630
         !
         1510 WRITE(6,1520)
         1520 FORMAT(/' PROGRAM TERMINATES BECAUSE NO SUITABLE T-EIGENVECTORS WERE IDENTIFIED'/&
         '  FOR ANY OF THE EIGENVALUES SUPPLIED.  PROBLEM COULD BE CAUSED'/&
         '  BY TOO SMALL A TVEC DIMENSION OR SIMPLY BE THAT IT WAS NOT POSSIBLE'/&
         '  TO IDENTIFY T-VECTORS.  USER SHOULD CHECK OUTPUT'/)
         GO TO 1630
         !
         1530 WRITE(6,1540) LVCONT,NTVEC,NGOOD
         1540 FORMAT(/' LVCONT FLAG =',I2,' AND NUMBER ',I5,' OF T-EIGENVECTORS COMPUTED N.E.'/&
         ' NUMBER',I5,' REQUESTED SO PROGRAM TERMINATES'/)
         GO TO 1630
         1550 WRITE(6,1560)
         1560 FORMAT(/' PROGRAM TERMINATES WITHOUT COMPUTING ANY RITZ VECTORS'/&
         ' BECAUSE ALL T-EIGENVECTORS WERE REJECTED AS NOT SUITABLE'/&
         ' PROBABLE CAUSE IS LACK OF CONVERGENCE OF THE EIGENVALUES'/)
         GO TO 1630
         !
         1570 WRITE(6,1580)
         1580 FORMAT(/' PROGRAM INDICATES THAT IT IS NOT POSSIBLE TO COMPUTE ANY OF THE'/&
         ' REQUESTED EIGENVECTORS. THEREFORE PROGRAM TERMINATES')
         DO 1590 J=1,NGOODC
           1590 IF(.NOT.silent_) WRITE(6,1600)  J,GOODEV(J),MP(J)
           1600 FORMAT(/4X,' J',11X,'GOODEV(J)',4X,'MP(J)'/I6,E20.12,I9)
           GO TO 1630
           !
           1610 WRITE(6,1620) MBETA,KMAXN
           1620 FORMAT(/' PROGRAM TERMINATES BECAUSE THE STORAGE ALLOTTED FOR THE BETA ARRAY',I8/&
           ' IS NOT SUFFICIENT FOR THE ENLARGED KMAX =',I8,' THAT THE PROGRAM WANTS'/&
           ' USER CAN ENLARGE THE ALPHA AND BETA ARRAYS AND RERUN THE PROGRAM.'/)
           !
           1630 CONTINUE
           !
           IF(ALLOCATED(TVEC))   DEALLOCATE(TVEC)
           IF(ALLOCATED(V1))     DEALLOCATE(V1)
           IF(ALLOCATED(V2))     DEALLOCATE(V2)
           IF(ALLOCATED(VS))     DEALLOCATE(VS)
           IF(ALLOCATED(ALPHA))  DEALLOCATE(ALPHA)
           IF(ALLOCATED(BETA))   DEALLOCATE(BETA)
           IF(ALLOCATED(GR))     DEALLOCATE(GR)
           IF(ALLOCATED(GC))     DEALLOCATE(GC)
           IF(ALLOCATED(G))      DEALLOCATE(G)
           IF(ALLOCATED(EVNEW))  DEALLOCATE(EVNEW)
           IF(ALLOCATED(TLAST))  DEALLOCATE(TLAST)
           IF(ALLOCATED(AMINGP)) DEALLOCATE(AMINGP)
           IF(ALLOCATED(TMINGP)) DEALLOCATE(TMINGP)
           IF(ALLOCATED(TERR))   DEALLOCATE(TERR)
           IF(ALLOCATED(ERR))    DEALLOCATE(ERR)
           IF(ALLOCATED(ERRDGP)) DEALLOCATE(ERRDGP)
           IF(ALLOCATED(RNORM))  DEALLOCATE(RNORM)
           IF(ALLOCATED(TBETA))  DEALLOCATE(TBETA)
           IF(ALLOCATED(MP))     DEALLOCATE(MP)
           IF(ALLOCATED(M1))     DEALLOCATE(M1)
           IF(ALLOCATED(M2))     DEALLOCATE(M2)
           IF(ALLOCATED(MA))     DEALLOCATE(MA)
           IF(ALLOCATED(ML))     DEALLOCATE(ML)
           IF(ALLOCATED(MINT))   DEALLOCATE(MINT)
           IF(ALLOCATED(MFIN))   DEALLOCATE(MFIN)
           IF(ALLOCATED(IDELTA)) DEALLOCATE(IDELTA)
           IF(ALLOCATED(MULEVA)) DEALLOCATE(MULEVA)
           IF(ALLOCATED(SVSEED)) DEALLOCATE(SVSEED)
           IF(ALLOCATED(SVSOLD)) DEALLOCATE(SVSOLD)
           IF(ALLOCATED(RHSEED)) DEALLOCATE(RHSEED)
           IF(ALLOCATED(IIL))    DEALLOCATE(IIL)
 

           IF(PRESENT(FILE2))  CLOSE(UNIT2_)
           IF(PRESENT(FILE3))  CLOSE(UNIT3_)
           IF(PRESENT(FILE4))  CLOSE(UNIT4_)
           IF(PRESENT(FILE5))  CLOSE(UNIT5_)
           IF(PRESENT(FILE10)) CLOSE(UNIT10_)

           END subroutine

